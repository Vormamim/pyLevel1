# Program to demonstrate arithmetic operators in Python

# Take two numbers as input from the user
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

# Addition operator
print("Addition: ", num1 + num2) # Output: Addition: 30

# Subtraction operator
print("Subtraction: ", num1 - num2) # Output: Subtraction: 10

# Multiplication operator
print("Multiplication: ", num1 * num2) # Output: Multiplication: 200

# Division operator (floating point) # Output: Division (floating point): 0.5
print("Division (floating point): ", num1 / num2)

# Division operator (floor) # Output: Division (floor): 0
print("Division (floor): ", num1 // num2)

# Modulo operator #
print("Modulo: ", num1 % num2) # Output: Modulo: 10

# Exponent operator
print("Exponent: ", num1 ** num2) # Output: Exponent: 100000000000000000000
